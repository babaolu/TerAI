void llama_kv_cache_seq_rm(llama_context* ctx, llama_seq_id, llama_pos p0, llama_pos p1) {
    printf("[stub] kv_cache_seq_rm(p0=%d, p1=%d) — kv had %zu tokens\n",
           p0, p1, ctx->kv_tokens.size());
    if (p1 < 0) {
        if ((size_t)p0 < ctx->kv_tokens.size())
            ctx->kv_tokens.resize(p0);
        else
            ctx->kv_tokens.clear();  // p0==0 && p1==-1 means "clear everything"
    }
}

