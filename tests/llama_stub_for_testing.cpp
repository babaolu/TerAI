static int g_sample_counter = 0;

llama_token llama_sampler_sample(llama_sampler*, llama_context*, int32_t) {
    // Deterministic fake generation: produce a few tokens then EOG.
    g_sample_counter++;
    if (g_sample_counter > 5) { g_sample_counter = 0; return 999999; }
    llama_token t = 200 + g_sample_counter;
    g_fake_vocab_rev[t] = "word" + std::to_string(g_sample_counter);
    return t;
}
